X = data.drop(columns = 'Result', axis=1)
Y = data['Result']

scaler = StandardScaler()

scaler.fit(X)

standardized_data = scaler.transform(X)

print(standardized_data)

X = standardized_data
Y = data['Result']

print(X)
print(Y)

X_train, X_test, Y_train, Y_test = train_test_split(X,Y, test_size = 0.2,random_state=0)

print(X.shape, X_train.shape, X_test.shape)

CLF = LazyClassifier(verbose=0,ignore_warnings=True)
models, predictions = CLF.fit(X_train, X_test, Y_train, Y_test)
models

models1 = models.sort_values(by=['F1 Score','Accuracy'],ascending=False)
models1 = pd.DataFrame(models1)
models1
models1.to_csv('30 algos Anemia.csv')

clf = models1.first_valid_index()
clf

clf1 = AdaBoostClassifier()

clf1.fit(X_train, Y_train)

X_train_prediction = clf1.predict(X_train)
training_data_accuracy = accuracy_score(X_train_prediction, Y_train)

print('Accuracy score of the training data : ', training_data_accuracy)

X_test_prediction = clf1.predict(X_test)
test_data_accuracy = accuracy_score(X_test_prediction, Y_test)

print('Accuracy score of the test data : ', test_data_accuracy)

input_data = (1,13.9,22.7,28.1,77.7)

# changing the input_data to numpy array
input_data_as_numpy_array = np.asarray(input_data)

# reshape the array as we are predicting for one instance
input_data_reshaped = input_data_as_numpy_array.reshape(1,-1)

# standardize the input data
std_data = scaler.transform(input_data_reshaped)
print(std_data)

prediction = clf1.predict(std_data)
print(prediction)

if (prediction[0] == 0):
  print('The person is not anemic')
